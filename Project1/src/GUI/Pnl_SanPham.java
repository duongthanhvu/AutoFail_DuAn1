    private void thayDoiDongChonTrenTblSanPham() {
        int viTri = tblSanPham.getSelectedRow();
        //Tránh lỗi NullPointerException
        if (viTri == -1) {
            return;
        }
        CustomObjectItem column1 = (CustomObjectItem) tblSanPham.getValueAt(viTri, 0);
        loadDuLieuVaoForm(column1.layMa());
    }

    private void loadDuLieuVaoForm(int idSanPham) {
        DTOSanPham ttsp = DALSanPham.layDuLieu(idSanPham);
        txtProductID.setText(Integer.toString(ttsp.getiDSanPham()));
        txtMaSP.setText(ttsp.getMaSP());
        txtTenSP.setText(ttsp.getTenSP());
        txtSoLuong.setText(Integer.toString(ttsp.getSoLuong()));
        txtDonViTinh.setText(ttsp.getDonViTinh());
        txtGiaNhap.setText(Integer.toString(ttsp.getGiaNhap()));
        txtGiaBan.setText(Integer.toString(ttsp.getGiaBan()));
        XuLyCbb.setItemDcChon(cbbLoaiSP, ttsp.getTenLoai());
        txtMoTa.setText(ttsp.getMoTa());
    }

    private void capNhatBangSanPham() {
        DefaultTableModel tblModel = (DefaultTableModel) tblSanPham.getModel();
        DTOSanPham[] arrSP;
        if (XuLyCbb.layMaCuaItemDcChon(cbbLocLoaiSP) == 0) {
            arrSP = DALSanPham.layDuLieu();
        } else {
            arrSP = DALSanPham.layDuLieuTheoMaLoai(XuLyCbb.layMaCuaItemDcChon(cbbLocLoaiSP));
        }
        tblModel.setRowCount(0);
        for (int i = 0; i < arrSP.length; i++) {
            Object[] item = new Object[6];
            item[0] = new CustomObjectItem(arrSP[i].getMaSP(), arrSP[i].getiDSanPham());
            item[1] = arrSP[i].getTenSP();
            item[2] = arrSP[i].getSoLuong();
            item[3] = arrSP[i].getGiaNhap();
            item[4] = arrSP[i].getGiaBan();
            item[5] = arrSP[i].getTenLoai();
            tblModel.addRow(item);
        }
    }

