    private boolean kiemTraTextField(){
        if(txtTenSP.getText().isEmpty()){
            XuLyThongBao.hienThiThongBao(new ThongBao("Chưa nhập tên", ThongBao.LOI));
            return false;
        }
        if(txtSoLuong.getText().isEmpty()){
            XuLyThongBao.hienThiThongBao(new ThongBao("Chưa nhập số lượng", ThongBao.LOI));
            return false;
        }
        if(txtDonViTinh.getText().isEmpty()){
            XuLyThongBao.hienThiThongBao(new ThongBao("Chưa nhập đơn vị tính", ThongBao.LOI));
            return false;
        }
        if(txtGiaNhap.getText().isEmpty()){
            XuLyThongBao.hienThiThongBao(new ThongBao("Chưa nhập giá nhập", ThongBao.LOI));
            return false;
        }
        if(txtGiaBan.getText().isEmpty()){
            XuLyThongBao.hienThiThongBao(new ThongBao("Chưa nhập giá bán", ThongBao.LOI));
            return false;
        }
        return true;
    }

    private void txtKhungTimKiemKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKhungTimKiemKeyReleased
        //Chừa lại phím lên xuống để lựa chọn item
        if (evt.getKeyCode() == evt.VK_UP || evt.getKeyCode() == evt.VK_DOWN) {
            popMenuTimKiem.requestFocus();
            return;
        }
        String keyWord = txtKhungTimKiem.getText();
        DTOSanPham arrItem[] = DALSanPham.searchBy(keyWord);
        popMenuTimKiem.removeAll();
        popMenuTimKiem.setVisible(false);
        for (DTOSanPham item : arrItem) {
            CustomMenuItem menuItem = new CustomMenuItem(item.getiDSanPham(), item.getTenSP());
            //Thêm sự kiện cho menuItem: khi click vào item -> đổ thông tin sản phẩm tương ứng lên form
            menuItem.addActionListener(new java.awt.event.ActionListener() {
                public void actionPerformed(ActionEvent e) {
                    txtKhungTimKiem.setText("");
                    loadDuLieuVaoForm(menuItem.getMa());
                }
            });
            popMenuTimKiem.add(menuItem);
        }
        popMenuTimKiem.show(txtKhungTimKiem, 0, txtKhungTimKiem.getHeight());
        txtKhungTimKiem.requestFocus();
    }//GEN-LAST:event_txtKhungTimKiemKeyReleased

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if(kiemTraTextField() == false){
            return;
        }
        DTOSanPham sp = new DTOSanPham(
                txtMaSP.getText(),
                txtTenSP.getText(),
                Integer.parseInt(txtSoLuong.getText()),
                txtDonViTinh.getText(),
                Integer.parseInt(txtGiaNhap.getText()),
                Integer.parseInt(txtGiaBan.getText()),
                XuLyCbb.layMaCuaItemDcChon(cbbLoaiSP),
                txtMoTa.getText());
        XuLyThongBao.hienThiThongBao(BLL.BLLSanPham.them(sp));
        capNhatBangSanPham();
    }//GEN-LAST:event_btnThemActionPerformed

